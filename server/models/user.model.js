const mongoose = require('mongoose');

//blueprint of user
const UserSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
});

//exporting the model
module.exports = mongoose.model('User', UserSchema);